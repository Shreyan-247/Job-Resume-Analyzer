const { GoogleGenerativeAI } = require("@google/generative-ai");
const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ model: "gemini-2.5-flash" });
const puppeteer = require("puppeteer");

// Add this to your existing ai.service.js file

/**
 * Helper to strip markdown code blocks if Gemini accidentally includes them
 */
const cleanHtmlResponse = (text) => {
    return text.replace(/```html/gi, '').replace(/```/g, '').trim();
};

const generateHtmlResume = async (resumeContent, selfDescription, jobDescription) => {

    const prompt = `You are an expert ATS Resume Writer, Recruiter, UI/UX Designer, and Frontend Developer.

        Using the candidate resume, self description, and target job description, generate a premium ATS-optimized resume as a COMPLETE HTML5 document with embedded CSS.
        Use bold texts dont leave much space in the side fit in one page, give proper width to the hr tags.Use black colour for all texts.
        INPUTS:

        Resume:
        ${resumeContent}

        Self Description:
        ${selfDescription}

        Job Description:
        ${jobDescription}

        Requirements:

        * Return ONLY valid HTML.
        * Do not return markdown or explanations.
        * Start with <!DOCTYPE html> and end with </html>.
        * Include all CSS inside a <style> tag.
        * Use modern, professional resume design.
        * Optimize content for the target job description.
        * ATS friendly and recruiter friendly.
        * Use Arial/Helvetica fonts.
        * White background with all black texts.
        * Strong typography hierarchy.
        * Professional spacing and section dividers.
        * Use Flexbox/Grid where appropriate.
        * Fit cleanly on A4 paper.
        * Minimize unnecessary whitespace.

        Include sections when information is available:

        * Header (Name, Email, Phone, LinkedIn, GitHub)
        * Professional Summary
        * Technical Skills
        * Projects
        * Experience
        * Education
        * Achievements
        * Certifications

        Formatting:

        * Skills in a two-column layout.
        * Company name left aligned, dates right aligned.
        * Projects should include technologies used and achievement-oriented bullet points.
        * Use concise, impactful language.
        * Prioritize keywords and skills from the job description.
        * The resume must fit on ONE A4 page.And not exceed 1 page.

        Minimize vertical spacing.
        Use compact layouts.
        Avoid large margins and padding.
        Use 10 body text.
        Use 14 section headings.
        Use two-column layouts whenever possible.
        Prioritize information density while maintaining readability.

        The final result should look like a professionally designed premium resume, not a plain text document.

        Generate the complete HTML document now.
        `;

    try {
        const result = await model.generateContent(prompt);
        const rawResponse = result.response.text();
        
        // Clean the response just in case Gemini adds markdown code blocks
        console.log(cleanHtmlResponse(rawResponse));

        return cleanHtmlResponse(rawResponse);
        
    } catch (error) {
        console.error("Error generating HTML resume from Gemini:", error);
        throw new Error("Failed to generate HTML resume");
    }
};

// Assuming 'resumeHtmlResponse' contains the HTML string generated by Gemini
const generatePdfResume = async (resumeContent, selfDescription, jobDescription) => {
    const resumeHtmlResponse=await generateHtmlResume(resumeContent, selfDescription, jobDescription);
    const browser = await puppeteer.launch();
    const page = await browser.newPage();

    // Pass the resume HTML directly into the page
    await page.setContent(resumeHtmlResponse, {
        waitUntil: 'networkidle2',
    });

    await page.setViewport({
        width: 1200,
        height: 1700,
        deviceScaleFactor: 2
    });

    await page.emulateMediaType("print");

    // Saves the PDF to tailored_resume.pdf.
    const pdf=await page.pdf({
        format: "A4",
        printBackground: true,
        margin: {
            top: "10mm",
            right: "10mm",
            bottom: "10mm",
            left: "10mm"
        }
    });

    await browser.close();

    return pdf;
};

 module.exports = { 
    generatePdfResume
};